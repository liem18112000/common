package com.application.common.specifications;

/**
 * The enum Search operation.
 */
public enum SearchOperation {
  /**
   * Equal search operation.
   */
  EQUAL,
  /**
   * Less than search operation.
   */
  LESS_THAN,
  /**
   * Less than or equal search operation.
   */
  LESS_THAN_OR_EQUAL,
  /**
   * Greater than search operation.
   */
  GREATER_THAN,
  /**
   * Timestamp greater than search operation.
   */
  TIMESTAMP_GREATER_THAN,
  /**
   * Timestamp greater than or equal search operation.
   */
  TIMESTAMP_GREATER_THAN_OR_EQUAL,
  /**
   * Timestamp less than search operation.
   */
  TIMESTAMP_LESS_THAN,
  /**
   * Timestamp less than or equal search operation.
   */
  TIMESTAMP_LESS_THAN_OR_EQUAL,
  /**
   * Greater than or equal search operation.
   */
  GREATER_THAN_OR_EQUAL,
  /**
   * Not equal search operation.
   */
  NOT_EQUAL
}
